import math


def cos(number, precision=4):
    """
    返回cos函数结果
    Args:
        number:
        precision:

    Returns:

    """
    result = round(math.cos(number), precision)
    return result


def sin(number, precision=4):
    """
    返回cos函数结果
    Args:
        number:
        precision:

    Returns:

    """
    result = round(math.sin(number), precision)
    return result
